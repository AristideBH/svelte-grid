<h1>Features</h1>

<ul>
  <li>Svelte 5 runes API — full <code>$state</code>, <code>$derived</code>, snippets</li>
  <li>Full TypeScript support with exported types</li>
  <li>No jQuery, no 3rd-party dependencies</li>
  <li>Draggable widgets</li>
  <li>Resizable widgets with min/max constraints</li>
  <li>Fixed / static widgets</li>
  <li>Layout serializable and restorable (plain JSON)</li>
  <li>Responsive breakpoints via <code>cols</code> definition</li>
  <li>Configurable gap between items</li>
  <li>Custom drag and resize handles</li>
  <li>Soft autoscroll during drag</li>
  <li>Fill-space mode (items rearrange around active item)</li>
</ul>

